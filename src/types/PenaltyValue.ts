/**
 * Represents the penalty values.
 */
export enum PenaltyValue {
  /**
   * No penalty.
   */
  PENALTY_NONE,

  /**
   * Drive-through.
   */
  PENALTY_DT,

  /**
   * Drive-through valid.
   */
  PENALTY_DT_VALID,

  /**
   * Stop and go.
   */
  PENALTY_SG,

  /**
   * Stop and go valid.
   */
  PENALTY_SG_VALID,

  /**
   * 30 seconds.
   */
  PENALTY_30,

  /**
   * 45 seconds.
   */
  PENALTY_45,
}
